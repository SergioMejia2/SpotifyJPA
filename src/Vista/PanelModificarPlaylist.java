    void actualizar(Playlist playlist, List<Cancion> canciones, List<BigInteger> ordenes)
    {
        jTextField1.setText(playlist.getNombrePlaylist());
        desplegarDatos(canciones, ordenes);
    }

    private void desplegarDatos(List<Cancion> canciones, List<BigInteger> ordenes)
    {
        String[] columns =
        {
            "Orden", "Cancion"
        };
        matriz = new Object[canciones.size()][columns.length];
        for (int i = 0; i < canciones.size(); i++)
        {
            matriz[i][0] = ordenes.get(i).toString();
            matriz[i][1] = canciones.get(i);
        }
        DefaultTableModel model = new DefaultTableModel(matriz, columns);
        jTable1.setModel(model);
        jScrollPane1.setViewportView(jTable1);
        repaint();
    }
