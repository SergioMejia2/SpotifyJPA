    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
        String nombreP = jTextField1.getText();
        TableModel table = jTable1.getModel();
        boolean correct = true;
        for (int i = 0; i < table.getRowCount(); i++)
        {
            System.out.println(table.getValueAt(i, 0));
            String a = (String)table.getValueAt(i, 0);
            
            BigInteger indice = new BigInteger(a);
            for (int j = 0; j < table.getRowCount(); j++)
            {
                if(i != j)
                {
                    String b = (String)table.getValueAt(j, 0);
                    BigInteger indice2 = new BigInteger(b);
                    if(indice.equals(indice2))
                    {
                        correct = false;
                        //System.out.println("i: "+i+" j: "+j+" indice: "+indice+" indice2: "+indice2);
                    }
                        
                }
            }
        }
        if(correct)
        {
            HashMap<BigInteger,Cancion> mapa = new HashMap<BigInteger,Cancion>();
            for (int i = 0; i < table.getRowCount(); i++)
            {
                mapa.put(new BigInteger((String)table.getValueAt(i, 0)), (Cancion)table.getValueAt(i, 1));
            }
            ventana.modificarPlaylist(mapa, nombreP);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Los ordenes de las canciones deben ser unicos", "ERROR!", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

