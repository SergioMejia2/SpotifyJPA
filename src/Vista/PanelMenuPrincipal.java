    public void desplegarDatos(List<Playlist> playlists)
    {
        String[] columns = {"Playlists"};
        matriz = new Object[playlists.size()][columns.length];
        for (int i = 0; i < playlists.size(); i++)
        {
            matriz[i][0] = playlists.get(i);
        }
        DefaultTableModel model = new DefaultTableModel(matriz, columns);
        jTable1.setModel(model);
        jScrollPane1.setViewportView(jTable1);
        jButton1.setEnabled(false);
        jButton4.setEnabled(false);
        jButton7.setEnabled(false);
        jButton8.setEnabled(false);
        jScrollPane1.setEnabled(false);
        jTable1.setEnabled(false);
        jTextField1.setEnabled(false);
        repaint();

    }
    
