    void actualizar(Playlist playlist)
    {
        String[] columns =
        {
            "Playlists"
        };
        Object newM[][] = new Object[matriz.length + 1][columns.length];
        for (int i = 0; i < matriz.length; i++)
        {
            newM[i][0] = matriz[i][0];
        }
        newM[matriz.length][0] = playlist;
        matriz = newM;
        DefaultTableModel model = new DefaultTableModel(newM, columns);
        jTable1.setModel(model);
        jScrollPane1.setViewportView(jTable1);
        repaint();
    }

    public Playlist getPlaylistSeleccionada()
    {
        return (Playlist)matriz[jTable1.getSelectedRow()][0];
    }

    void Login(String username, String password)
    {
        if(username.equalsIgnoreCase("admin") && password.equals("admin"))
        {
            jButton1.setEnabled(true);
            jButton4.setEnabled(true);
            jButton7.setEnabled(true);
            jButton8.setEnabled(false);
            jScrollPane1.setEnabled(false);
            jTable1.setEnabled(false);
            jTextField1.setEnabled(false);
        }
        else
        {

            jButton7.setEnabled(true);
            jButton8.setEnabled(true);
            jScrollPane1.setEnabled(true);
            jTable1.setEnabled(true);
            jTextField1.setEnabled(true);
        }
    }
